public void ValidarUsuario(String user,String Contraseña){
    NodoUsuarios Activo=ListasGenerales.listaUser.AbrirUsuario(user, Contraseña);
    
    if(user.equals(Album.administrador) && Contraseña.equals(Album.codigo)){
        //System.out.println("Ingreso");
        Administrador admi=new Administrador();
        this.dispose();
    }else if(Activo!=null){
        VentanaUsuarios v1=new VentanaUsuarios(Activo);
        Integer cui=Activo.getCui();
        ListasGenerales.listaUser.Ingreso(cui);
        this.dispose();
    }else{
        
        JOptionPane.showMessageDialog(null, "El Usuario no esta Registrado");
        
        password.setText("");
        
    }
 }

