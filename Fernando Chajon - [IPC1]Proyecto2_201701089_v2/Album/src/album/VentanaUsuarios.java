    private void AbrirNormalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirNormalActionPerformed
        MostrarEstampa.setText("");
        InformacionEstampa.setText("");
        VentanaUsuarios.actual=VentanaUsuarios.activo.sobres.CrearSobre();
        
        Integer abierto=VentanaUsuarios.activo.getAbiertos();
        abierto=abierto+1;
        VentanaUsuarios.activo.setAbiertos(abierto);
        this.TotalSobres.setText("Sobres Abiertos: "+VentanaUsuarios.activo.getAbiertos());
        
        RecienAbierto.setVisible(true);
        VentanaUsuarios.estampa=actual.listaEstampa.ObtenerPrimera();
        VentanaUsuarios.equipo=VentanaUsuarios.activo.equipos.ObtenerEquipo(VentanaUsuarios.estampa.getCodigo());
        VentanaUsuarios.jugador=VentanaUsuarios.equipo.jugadores.ObtenerJ(VentanaUsuarios.estampa.getNombre());
        MostrarEstampa.setIcon(new ImageIcon(VentanaUsuarios.estampa.getRuta()));
        InformacionEstampa.setText("Nombre: "+VentanaUsuarios.estampa.getNombre()
        +" Posicion: "+VentanaUsuarios.jugador.getPosicion()+" Equipo: "+VentanaUsuarios.equipo.getNombre()+" Rareza: "
        +VentanaUsuarios.estampa.getRareza()+" Estampa No: "+VentanaUsuarios.estampa.getCantidad());
    }//GEN-LAST:event_AbrirNormalActionPerformed

