  public void MostrarTabla(){
      String tabla[][]=new String[ListasGenerales.listaCambios.ObtenerTama()][4];
      NodoCambio aux=ListasGenerales.listaCambios.ObtenerPrimero();
      for(int j=0;j<ListasGenerales.listaCambios.ObtenerTama();j++){
          tabla[j][0]=aux.getNombre();
          tabla[j][1]=aux.getEquipo();
          tabla[j][2]=Integer.toString(aux.getRara());
          tabla[j][3]=aux.getUser();
          aux=ListasGenerales.listaCambios.ObtenerSiguiente(aux);
      }
      
       JTMostrar.setModel(new javax.swing.table.DefaultTableModel(
            tabla,
            new String [] {
                "Nombre Jugador", "Equipo ", "Rareza", "Usuario "
            }
        ));
      
  }
    
    
    
    
    
