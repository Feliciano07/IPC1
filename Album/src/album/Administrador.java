    private void AbrirArchivoEsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirArchivoEsActionPerformed
        String todo="";
        String linea="";
        JFileChooser file=new JFileChooser();
        FileNameExtensionFilter ecys=new FileNameExtensionFilter("*.ecys","ecys");
        file.setFileFilter(ecys);
        int seleccion=file.showOpenDialog(file);
        if(seleccion==JFileChooser.APPROVE_OPTION){
            File archivo=file.getSelectedFile();//Guarcho el archivo en un tipo archivo
            this.DireccionArchivoEs.setText(archivo.getAbsolutePath());//muestro la direccion del archivo 
            
            try {
                FileReader leer=new FileReader(archivo);//creo un nuevo archivo en donde le mando el archivo que seleccione
                BufferedReader leendo=new BufferedReader(leer);//es un metodo que me permite leer linea por linea
                while((linea=leendo.readLine())!=null){ //verifico que la linea que esta leendo no este vacia
                    todo+=linea+"\n";//tomo un string y lo concateno con la linea que viene
                    
                }
                leendo.close(); //cierro el archivo
                this.AreaArchivoEs.setText(todo);//muestro en el jtexarea
            } catch (IOException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_AbrirArchivoEsActionPerformed

