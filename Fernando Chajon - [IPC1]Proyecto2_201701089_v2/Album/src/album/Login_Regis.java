    private void AceptarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarRegistroActionPerformed
        Login_Regis.cui=Integer.parseInt(Lcui.getText());
        Login_Regis.nombre=Lnombre.getText();
        Login_Regis.fecha=Lfecha.getText();
        Login_Regis.correo=Lcorreo.getText();
        Login_Regis.user1=Luser.getText();
        Login_Regis.contrase1=new String(Lpas.getPassword());
        Login_Regis.contrase2=new String(Lpas1.getPassword());
        
        if(Login_Regis.contrase1.equals(Login_Regis.contrase2)){
            
            
           
            ListasGenerales.listaUser.NuevoUsuario(cui, nombre, fecha, correo, user1, contrase1);
            
            
            JOptionPane.showMessageDialog(null, "El Usuario se ha creado");
            this.RegistroUsuario.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Las contraseñas no coninciden, por favor intentelo de nuevo");
             
            this.Lpas.setText("");
            this.Lpas1.setText("");
        }
        
    }//GEN-LAST:event_AceptarRegistroActionPerformed

